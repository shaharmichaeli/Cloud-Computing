package exercise3;

public class UserBoundary {
	private String userEmail;

	public UserBoundary() {
		super();
	}

	public UserBoundary(String userEmail) {
		super();
		this.userEmail = userEmail;
	}

	public String getUserEmail() {
		return userEmail;
	}

	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	
	

}
